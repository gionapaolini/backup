<h2>About</h2>
<p>
  We are a student-led organisation that integrates Design Culture to provide a platform for creative problem-solving across real social issues. It is our goal to foster an exchange of accessible visions, knowledge and strategies between students, innovators, professionals and designers. This is exactly what we do at the second edition of our annual This Is Not a Design Conference on the 23rd — 24th of September 2017.
 <br> <br>
  Based in Maastricht, we work at the core of the Meuse–Rhine Euroregion, a great location to put the approach of design culture into practice due to the diverse and international environment and its central location in Europe.

</p>

